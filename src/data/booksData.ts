/* ---------- Утилиты ---------- */

export function getPrevNext(currentId: string): { prev: BookData | null; next: BookData | null } {
  const idx = books.findIndex((b) => b.id === currentId);
  const prev = idx > 0 ? books[idx - 1] : null;
  const next = idx < books.length - 1 ? books[idx + 1] : null;
  return { prev, next };
}

/* Фильтрация: по арке + поисковому запросу */
export function filterBooks(
  arc: ArcFilter,
  query: string,
): BookData[] {
  const arcIds = arcMapping[arc] || [];
  let result = arcIds.length === 0
    ? books
    : books.filter((b) => arcIds.includes(b.id));

  if (query.trim()) {
    const q = query.toLowerCase();
    result = result.filter(
      (b) =>
        b.title.toLowerCase().includes(q) ||
        b.description.toLowerCase().includes(q) ||
        b.fullDescription.toLowerCase().includes(q),
    );
  }

  return result;
}

